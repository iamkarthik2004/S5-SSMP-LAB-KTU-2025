DATA SEGMENT
NUMBERS DB 05H, 02H, 09H, 01H, 06H
COUNT DB 05
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
    MOV AX,DATA
    MOV DS,AX

    MOV CL,COUNT
    DEC CL             ; Outer loop counter

OUTER:
    MOV CH,CL
    LEA SI,NUMBERS

INNER:
    MOV AL,[SI]
    CMP AL,[SI+1]
    JBE SKIP
    XCHG AL,[SI+1]
    MOV [SI],AL
SKIP:
    INC SI
    DEC CH
    JNZ INNER
    DEC CL
    JNZ OUTER

    INT 3
CODE ENDS
END START
